#include "3-calc.h"

/**
 * op_add - add
 *
 * @a: first
 * @b: second
 *
 * Return: a+b as an integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub
 *
 * @a: first
 * @b: second
 *
 * Return: a-b as an integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul
 *
 * @a: first
 * @b: second
 *
 * Return: a*b as an integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div
 *
 * @a: first
 * @b: second
 *
 * Return: a/b as an integer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod
 *
 * @a: first
 * @b: second
 *
 * Return: a%b as an integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
