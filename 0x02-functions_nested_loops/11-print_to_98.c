/**
 * print_to_98 - jfdhjh uhfuh ydg y ghg
 *
 * @n: jdhfujhn hu h h f
 *
*/
void print_to_98(int n)
{
	int count;

	if (n > 98)
		for (count = n; count > 98; count--)
			printf("%d, ", count);
	else
		for (count = n; count < 98; count++)
			prinf("%d, ", count);
	prinf("98\n");
}
